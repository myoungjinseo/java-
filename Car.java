package Hw11_1;

class Car {

	   private String color;// 색상

	   private int price;// 가격

	   // 색상과 가격을 매개변수로 받아 초기화하는 생성자
	   public Car(String color,int price){
		   this.color = color;
		   this.price = price;
		   
	   }

	   public String getColor() {

	      return color;

	   }

	 

	   public int getPrice() {

	      return price;

	   }

	}
